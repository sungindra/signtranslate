class UserSerializer < ActiveModel::Serializer
  attributes :id, :email, :name
  # attribute :push_token
  attribute :authentication_token, if: :jwt?

  def authentication_token
    object.generate_jwt
  end

  def jwt?
    true if scope[:jwt].present?
  end
end
